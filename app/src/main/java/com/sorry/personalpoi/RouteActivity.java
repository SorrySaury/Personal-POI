    class MyTransitDlg extends Dialog {

        private List<? extends RouteLine> mtransitRouteLines;
        private ListView transitRouteList;
        private RouteLineAdapter mTransitAdapter;

        OnItemInDlgClickListener onItemInDlgClickListener;

        public MyTransitDlg(Context context, int theme) {
            super(context, theme);
        }

        public MyTransitDlg(Context context, List<? extends RouteLine> transitRouteLines, RouteLineAdapter.Type
                type) {
            this(context, 0);
            mtransitRouteLines = transitRouteLines;
            mTransitAdapter = new RouteLineAdapter(context, mtransitRouteLines, type);
            requestWindowFeature(Window.FEATURE_NO_TITLE);
        }

        @Override
        public void setOnDismissListener(OnDismissListener listener) {
            super.setOnDismissListener(listener);
        }

        @Override
        protected void onCreate(Bundle savedInstanceState) {
            super.onCreate(savedInstanceState);
            setContentView(R.layout.activity_transit_dialog);

            transitRouteList = (ListView) findViewById(R.id.transitList);
            transitRouteList.setAdapter(mTransitAdapter);

            transitRouteList.setOnItemClickListener(new AdapterView.OnItemClickListener() {

                @Override
                public void onItemClick(AdapterView<?> parent, View view, int position, long id) {
                    onItemInDlgClickListener.onItemClick(position);
                    //mBtnPre.setVisibility(View.VISIBLE);
                   // mBtnNext.setVisibility(View.VISIBLE);
                    dismiss();
                    hasShownDialogue = false;
                }
            });
        }

        public void setOnItemInDlgClickLinster(OnItemInDlgClickListener itemListener) {
            onItemInDlgClickListener = itemListener;
        }

    }





    @Override
    public void onClick(View v) {
        int i = v.getId();
        PlanNode stNode = PlanNode.withCityNameAndPlaceName(city,from.getText().toString());
        PlanNode enNode = PlanNode.withCityNameAndPlaceName(city,to.getText().toString());
        Log.i("routeinfo",city+":"+from.getText().toString()+"->"+to.getText().toString());
        if(i == R.id.from){
            Intent intent = new Intent(this,PoiSearchActivity.class);
            intent.putExtra("city",city);
            startActivityForResult(intent,bfrom);
        }
        if(i == R.id.to){
            Intent intent = new Intent(this,PoiSearchActivity.class);
            intent.putExtra("city",city);
            startActivityForResult(intent,bto);
        }
        if(i == R.id.exchange){
            tmp = from.getText().toString();
            from.setText(to.getText().toString());
            to.setText(tmp);
        }
        if(i == R.id.drive){
            baiduMap.clear();
            routePlanSearch.drivingSearch((new DrivingRoutePlanOption())
                    .from(stNode).to(enNode).policy(DrivingRoutePlanOption.DrivingPolicy.ECAR_DIS_FIRST)
                    .trafficPolicy(DrivingRoutePlanOption.DrivingTrafficPolicy.ROUTE_PATH_AND_TRAFFIC));
            nowSearchType = 1;
        }
        if(i == R.id.bike){
            baiduMap.clear();
            routePlanSearch.bikingSearch((new BikingRoutePlanOption())
                    .from(stNode).to(enNode));
            nowSearchType = 4;
        }
        if(i == R.id.walk){
            baiduMap.clear();
            routePlanSearch.walkingSearch((new WalkingRoutePlanOption())
                    .from(stNode).to(enNode));
            nowSearchType = 3;
        }


    }
